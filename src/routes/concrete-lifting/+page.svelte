			<p class="mt-6 text-lg leading-7 text-gray-600">
				Helical piers are a permanent solution for concrete lifting. They are installed under the
				concrete slab and extend down to stable soil. The piers support the slab and prevent it from
				sinking further. Depending on the application, we also recommend push piers and slab piers.
			</p>
		</div>
