<section>
	<h2>Types of Concrete Lifting</h2>
	<p>Real foundation experts recommend two main types of concrete lifting:</p>
	<div>
		<ul>
			<li>
				Polyurethane foam injection: Polyurethane foam injection is a newer, more efficient method
				of concrete lifting. It involves drilling holes in the concrete and injecting a polyurethane
				foam under the slab. The foam expands and raises the slab to its original level. It is
				closed-cell (impermeable to water) and extremely strong, capable of lifting heavy loads of
				thousands of pounds.
			</li>
			<li>
				Helical piers: Helical piers are a permanent solution for concrete lifting. They are
				installed under the concrete slab and extend down to stable soil. The piers support the slab
				and prevent it from sinking further. Depending on the application, we also recommend push
				piers and slab piers.
			</li>
		</ul>
	</div>
	<p>
		Another common method of concrete lifting is called mudjacking. It involves drilling holes in
		the concrete and injecting a mixture of soil, sand, and water under the slab. The mixture
		expands and raises the slab to its original level. We do not recommend this type of repair.
		Closed-cell, structural polyurethane foam injection has far surpassed this outdated technology.
	</p>
</section>
<section>
	<h2>Choosing the Right Concrete Lifting Method</h2>
	<p>
		The best method of concrete lifting for you will depend on the size and severity of the problem.
		If the concrete is only slightly sunken, mudjacking may be a sufficient solution. However, if
		the concrete is severely sunken or cracked, polyurethane foam injection or helical piers may be
		a better option.
